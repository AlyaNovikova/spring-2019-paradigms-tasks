  'empty', 'singleton' и 'Map.null' работают за /O(1)/.
  'fromList', 'toAscList' и 'size' работают за /O(n)/, где /n/ – количество вершин дерева.
  Остальные функции работают за /O(h)/, где /h/ – высота дерева,
  причём каждая функция должна спускаться вниз по дереву не больше одного раза.

  Скорее всего, при реализации вам потребуется функция 'merge'.
-}
instance Map NaiveTree where
    empty = undefined

    singleton = undefined

    toAscList = undefined

    alter = undefined

    lookup = undefined

    null = undefined

    size = undefined
